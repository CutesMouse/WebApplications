function convert_json(source) {
    let ary = source.split("\n");
    let result = [];
    for (const line of ary) {
        let content = line.split("/");
        let voc = content[0];
        let accent = parseInt(content[1]);
        let voc_chinese = content[2];
        let sentences = [];
        let kanji = undefined;
        let reading = undefined;
        if (voc.indexOf('(') !== -1) {
            kanji = voc.substring(0, voc.indexOf('('));
            reading = voc.substring(voc.indexOf('(') + 1, voc.indexOf(')'));
        }
        for (let i = 3; (i + 1) < content.length; i += 2) {
            sentences.push({
                "sentence": content[i],
                "translation": content[i + 1],
                "blank": content[i].split('{')[1].split('}')[0]
            });
        }
        result.push({
            "sentences": sentences,
            "voc": voc,
            "chinese": voc_chinese,
            "kanji": kanji,
            "reading": reading,
            "accent": accent
        });
    }
    return result;
}

function shuffle(items) {
    let cached = items.slice(0), temp, i = cached.length, rand;
    while (--i) {
        rand = Math.floor(i * Math.random());
        temp = cached[rand];
        cached[rand] = cached[i];
        cached[i] = temp;
    }
    return cached;
}

function getRandomInt(max) {
    return Math.floor(Math.random() * max);
}

function random_sentence(problem) {
    let sentences = problem.sentences;
    return sentences[getRandomInt(sentences.length)];
}
